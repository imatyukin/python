#!/usr/bin/env python3

age = 37

if (age < 2):
    print("младенец")
elif (age >= 2 and age < 4):
    print("малыш")
elif (age >= 4 and age < 13):
    print("ребёнок")
elif (age >= 13 and age < 20):
    print("подросток")
elif (age >= 20 and age < 65):
    print("взрослый")
else:
    print("пожилой человек")
